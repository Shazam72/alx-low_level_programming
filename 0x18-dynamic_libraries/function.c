#include "main.h"

/**
 * add - addition
 * @a: first operand
 * @b: second operand
 * Return: sum of 2 numbers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtraction
 * @a: first operand
 * @b: second operand
 * Return: difference of 2 numbers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplication
 * @a: first operand
 * @b: second operand
 * Return: product of the numbers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division
 * @a: first operand
 * @b: second operand
 * Return: division of the numbers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulo
 * @a: first operand
 * @b: second operand
 * Return: modulo of the numbers
 */
int mod(int a, int b)
{
	return (a % b);
}
